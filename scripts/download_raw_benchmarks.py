"""Download raw benchmark data used by the project.

Raw data is stored under ``data/raw``. Converted project inputs are generated by
``scripts/install_report_benchmarks.py``.
"""

from __future__ import annotations

import html
import io
import re
import shutil
import zipfile
from pathlib import Path
from urllib.parse import urljoin
from urllib.request import urlopen

from install_report_benchmarks import CVRPLIB_SELECTION, MTSPLIB_SELECTION, TSPLIB_SELECTION


TSPLIB_ARCHIVE_URL = "https://github.com/mastqe/tsplib/archive/refs/heads/master.zip"
CVRPLIB_INSTANCES_URL = "https://galgos.inf.puc-rio.br/cvrplib/en/instances"
MTSPLIB_URL = "https://profs.info.uaic.ro/mihaela.breaban/mtsplib/MinMaxMTSP/"


def fetch_bytes(url: str, timeout: int = 90) -> bytes:
    with urlopen(url, timeout=timeout) as response:
        return response.read()


def fetch_text(url: str, timeout: int = 90) -> str:
    return fetch_bytes(url, timeout=timeout).decode("utf-8", errors="replace")


def download_tsplib_archive() -> int:
    raw_dir = Path("data/raw/tsplib")
    raw_dir.mkdir(parents=True, exist_ok=True)
    selected_names = set(TSPLIB_SELECTION) | set(MTSPLIB_SELECTION)

    archive = fetch_bytes(TSPLIB_ARCHIVE_URL, timeout=180)
    count = 0
    with zipfile.ZipFile(io.BytesIO(archive)) as zip_file:
        for member in zip_file.namelist():
            if not member.lower().endswith(".tsp"):
                continue
            file_name = Path(member).name
            if Path(file_name).stem not in selected_names:
                continue
            (raw_dir / file_name).write_bytes(zip_file.read(member))
            count += 1
    return count


def parse_cvrplib_download_links() -> dict[str, str]:
    text = fetch_text(CVRPLIB_INSTANCES_URL)
    pattern = re.compile(
        r'<a\s+href="(?P<href>[^"]*/download/instance/\d+)"[^>]*>\s*'
        r"(?P<name>[A-Za-z0-9_.-]+)\s*</a>",
        re.IGNORECASE,
    )
    return {
        html.unescape(match.group("name").strip()): urljoin(
            CVRPLIB_INSTANCES_URL,
            html.unescape(match.group("href").strip()),
        )
        for match in pattern.finditer(text)
    }


def download_cvrplib_selected() -> int:
    raw_dir = Path("data/raw/cvrplib")
    raw_dir.mkdir(parents=True, exist_ok=True)
    links = parse_cvrplib_download_links()

    count = 0
    for name in CVRPLIB_SELECTION:
        if name not in links:
            raise KeyError(f"CVRPLIB download link not found for {name}")
        (raw_dir / f"{name}.vrp").write_text(fetch_text(links[name], timeout=60), encoding="utf-8")
        count += 1
    return count


def download_mtsplib_raw() -> tuple[int, int]:
    tsp_dir = Path("data/raw/mtsplib_minmax/tsp")
    tour_dir = Path("data/raw/mtsplib_minmax/tours")
    tsp_dir.mkdir(parents=True, exist_ok=True)
    tour_dir.mkdir(parents=True, exist_ok=True)

    tsp_count = 0
    for name in MTSPLIB_SELECTION:
        source = Path("data/raw/tsplib") / f"{name}.tsp"
        if not source.exists():
            raise FileNotFoundError(f"missing TSPLIB source for MTSPLIB: {source}")
        shutil.copyfile(source, tsp_dir / source.name)
        tsp_count += 1

    index = fetch_text(urljoin(MTSPLIB_URL, "index.html"))
    hrefs = re.findall(r'href="([^"]*tours[^"]*\.txt)"', index)
    tour_count = 0
    for href in hrefs:
        file_name = Path(href).name
        (tour_dir / file_name).write_text(fetch_text(urljoin(MTSPLIB_URL, href)), encoding="utf-8")
        tour_count += 1

    return tsp_count, tour_count


def main() -> None:
    tsplib_count = download_tsplib_archive()
    cvrplib_count = download_cvrplib_selected()
    mtsplib_tsp_count, mtsplib_tour_count = download_mtsplib_raw()
    print(f"TSPLIB raw .tsp files: {tsplib_count}")
    print(f"CVRPLIB selected raw .vrp files: {cvrplib_count}")
    print(f"MTSPLIB raw .tsp files: {mtsplib_tsp_count}")
    print(f"MTSPLIB raw tour files: {mtsplib_tour_count}")


if __name__ == "__main__":
    main()
