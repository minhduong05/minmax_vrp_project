"""Compare per-algorithm final test CSV files.

Input files should be generated by scripts/test_best_algorithm.py. The script
aggregates repeated solver seeds per instance first, then compares algorithms on
equal instance weight.
"""

from __future__ import annotations

import argparse
import csv
import statistics
import sys
from collections import defaultdict
from datetime import datetime
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
if str(ROOT) not in sys.path:
    sys.path.insert(0, str(ROOT))

DEFAULT_INPUT_GLOB = "output/algorithm_comparison/*_best_test_runs.csv"
DEFAULT_OUTPUT_DIR = "output/algorithm_comparison"

SUMMARY_HEADER = [
    "timestamp",
    "algorithm",
    "config_name",
    "instances",
    "runs",
    "feasible_rate",
    "mean_max_route",
    "std_instance_mean_max_route",
    "median_instance_mean_max_route",
    "mean_total_distance",
    "mean_balance",
    "mean_runtime",
    "mean_iterations",
    "win_rate",
    "rank_sum",
]

DETAIL_HEADER = [
    "timestamp",
    "instance",
    "family",
    "n",
    "k",
    "algorithm",
    "config_name",
    "runs",
    "feasible_rate",
    "mean_max_route",
    "mean_total_distance",
    "mean_balance",
    "mean_runtime",
    "mean_iterations",
    "rank_by_max_route",
    "is_winner",
]


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--inputs",
        nargs="+",
        help="Explicit run CSV files. Defaults to output/algorithm_comparison/*_best_test_runs.csv.",
    )
    parser.add_argument("--input-glob", default=DEFAULT_INPUT_GLOB)
    parser.add_argument("--output-dir", default=DEFAULT_OUTPUT_DIR)
    parser.add_argument("--stamp")
    return parser


def read_rows(paths: list[Path]) -> list[dict[str, str]]:
    rows = []
    for path in paths:
        with path.open(newline="", encoding="utf-8") as file:
            rows.extend(csv.DictReader(file))
    return rows


def mean(values):
    return statistics.mean(values) if values else 0.0


def aggregate_by_instance(rows: list[dict[str, str]], timestamp: str):
    grouped: dict[tuple[str, str], list[dict[str, str]]] = defaultdict(list)
    for row in rows:
        grouped[(row["instance"], row["algorithm"])].append(row)

    detail_rows = []
    for (instance, algorithm), group in grouped.items():
        max_routes = [float(row["max_route"]) for row in group]
        total_distances = [float(row["total_distance"]) for row in group]
        balances = [float(row["balance"]) for row in group]
        runtimes = [float(row["runtime"]) for row in group]
        iterations = [int(float(row["iterations"])) for row in group]
        feasible = [row["feasible"] == "yes" for row in group]
        first = group[0]
        detail_rows.append(
            {
                "timestamp": timestamp,
                "instance": instance,
                "family": first["family"],
                "n": int(float(first["n"])),
                "k": int(float(first["k"])),
                "algorithm": algorithm,
                "config_name": first["config_name"],
                "runs": len(group),
                "feasible_rate": sum(feasible) / len(feasible),
                "mean_max_route": mean(max_routes),
                "mean_total_distance": mean(total_distances),
                "mean_balance": mean(balances),
                "mean_runtime": mean(runtimes),
                "mean_iterations": mean(iterations),
                "rank_by_max_route": 0,
                "is_winner": 0,
            }
        )

    by_instance: dict[str, list[dict[str, object]]] = defaultdict(list)
    for row in detail_rows:
        by_instance[str(row["instance"])].append(row)
    for group in by_instance.values():
        group.sort(key=lambda row: (float(row["mean_max_route"]), str(row["algorithm"])))
        for rank, row in enumerate(group, start=1):
            row["rank_by_max_route"] = rank
            row["is_winner"] = 1 if rank == 1 else 0
    detail_rows.sort(key=lambda row: (str(row["instance"]), int(row["rank_by_max_route"])))
    return detail_rows


def summarize(detail_rows: list[dict[str, object]], timestamp: str):
    grouped: dict[str, list[dict[str, object]]] = defaultdict(list)
    for row in detail_rows:
        grouped[str(row["algorithm"])].append(row)

    summary_rows = []
    for algorithm, group in grouped.items():
        max_routes = [float(row["mean_max_route"]) for row in group]
        total_distances = [float(row["mean_total_distance"]) for row in group]
        balances = [float(row["mean_balance"]) for row in group]
        runtimes = [float(row["mean_runtime"]) for row in group]
        iterations = [float(row["mean_iterations"]) for row in group]
        feasible_rates = [float(row["feasible_rate"]) for row in group]
        winners = [int(row["is_winner"]) for row in group]
        ranks = [int(row["rank_by_max_route"]) for row in group]
        runs = [int(row["runs"]) for row in group]
        summary_rows.append(
            {
                "timestamp": timestamp,
                "algorithm": algorithm,
                "config_name": group[0]["config_name"],
                "instances": len(group),
                "runs": sum(runs),
                "feasible_rate": mean(feasible_rates),
                "mean_max_route": mean(max_routes),
                "std_instance_mean_max_route": statistics.pstdev(max_routes)
                if len(max_routes) > 1
                else 0.0,
                "median_instance_mean_max_route": statistics.median(max_routes),
                "mean_total_distance": mean(total_distances),
                "mean_balance": mean(balances),
                "mean_runtime": mean(runtimes),
                "mean_iterations": mean(iterations),
                "win_rate": sum(winners) / len(winners),
                "rank_sum": sum(ranks),
            }
        )
    summary_rows.sort(
        key=lambda row: (
            float(row["mean_max_route"]),
            -float(row["win_rate"]),
            float(row["mean_total_distance"]),
        )
    )
    return summary_rows


def write_csv(path: Path, rows: list[dict[str, object]], header: list[str]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", newline="", encoding="utf-8") as file:
        writer = csv.DictWriter(file, fieldnames=header)
        writer.writeheader()
        writer.writerows(rows)


def main() -> int:
    args = build_parser().parse_args()
    if args.inputs:
        paths = [ROOT / path for path in args.inputs]
    else:
        paths = sorted(ROOT.glob(args.input_glob))
    if not paths:
        raise FileNotFoundError("No *_best_test_runs.csv files found.")

    timestamp = datetime.now().isoformat(timespec="seconds")
    stamp = args.stamp or datetime.now().strftime("%Y%m%d_%H%M%S")
    rows = read_rows(paths)
    detail_rows = aggregate_by_instance(rows, timestamp)
    summary_rows = summarize(detail_rows, timestamp)

    output_dir = ROOT / args.output_dir
    detail_path = output_dir / f"{stamp}_best_algorithms_comparison_by_instance.csv"
    summary_path = output_dir / f"{stamp}_best_algorithms_comparison_summary.csv"
    write_csv(detail_path, detail_rows, DETAIL_HEADER)
    write_csv(summary_path, summary_rows, SUMMARY_HEADER)

    print(f"Read {len(paths)} run file(s).")
    print(f"Wrote detail: {detail_path}")
    print(f"Wrote summary: {summary_path}")
    print("\nRanking by mean_max_route:")
    for rank, row in enumerate(summary_rows, start=1):
        print(
            f"{rank}. {row['algorithm']}: "
            f"mean_max_route={float(row['mean_max_route']):.3f}, "
            f"win_rate={float(row['win_rate']):.3f}, "
            f"runtime={float(row['mean_runtime']):.3f}s"
        )
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
